	.extern	biosinit, _wboot
	
	.global	bios, func0, func1

	unsegm
	sect .text

!------------------------------------------------------------------------------
! func0 
!   Initialization

func0:
	call	biosinit
	clr	r7
	
	ret
!------------------------------------------------------------------------------
! func1 
!   Warm Boot

func1:
	call	_wboot
	ret
	